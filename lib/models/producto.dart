class Producto {
  String id;
  String titulo;
  String categoria;
  double precio;
  int stock;
  String imagen;
  String descripcion;
  String fuente;

  Producto({
    required this.id,
    required this.titulo,
    required this.categoria,
    required this.precio,
    required this.stock,
    required this.imagen,
    required this.descripcion,
    required this.fuente,
  });
}
